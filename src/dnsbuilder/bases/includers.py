class BindIncluder(BaseIncluder):
    """
    BIND-style configuration assembler.

    Uses brace counting to inject includes into existing blocks.
    include_tpl is fetched from BindSection ('include "{path}";').
    """

    def inject(self, content: str, section: str, lines: List[str]) -> Tuple[str, bool]:
        """
        Inject include lines into an existing block using brace counting.

        BIND blocks end with '}', so we count braces to find the insertion point.
        """
        section_info = self.get_section_info(section)
        if section_info and section_info.block_pattern:
            pattern = re.compile(section_info.block_pattern, re.IGNORECASE)
        else:
            pattern = re.compile(rf'\b{re.escape(section)}\s*\{{', re.IGNORECASE)

        match = pattern.search(content)
        if not match:
            return content, False

        start_pos = match.end()
        brace_count = 1
        pos = start_pos

        while pos < len(content) and brace_count > 0:
            if content[pos] == '{':
                brace_count += 1
            elif content[pos] == '}':
                brace_count -= 1
            pos += 1

        if brace_count != 0:
            logger.warning(f"Unbalanced braces in block '{section}'")
            return content, False

        insert_pos = pos - 1
        insert_content = "".join(lines)

        updated = content[:insert_pos] + insert_content + content[insert_pos:]
        return updated, True


