class PdnsRecursorIncluder(Includer):
    """
    PowerDNS Recursor configuration assembler.

    PowerDNS Recursor uses a simple key=value format without block structure.
    Configuration fragments are moved to an include directory and referenced
    via the include-dir directive.
    """
    _tmpl = '\n# include {config_line}'
    _write = "\n# Auto Generated by DNS Builder\ninclude-dir={include_dir}\n"

    def assemble(self) -> None:
        """
        Assemble all pending fragments for PowerDNS Recursor.

        PowerDNS Recursor uses include-dir directive to include all files
        from a directory. Fragments are moved to the include directory.
        """
        if not self.main:
            logger.warning("No global main config found for PowerDNS Recursor")
            return

        all_fragments = []
        for section in self.get_all_sections():
            all_fragments.extend(self.get_pendings(section))

        for fragment in all_fragments:
            new_fragment = self._move_to_include_dir(fragment)
            if new_fragment:
                self._add_include_comment(self.main, new_fragment)

    def _move_to_include_dir(self, fragment: ConfigFragment) -> Optional[ConfigFragment]:
        """
        Move a fragment to the include directory.

        Args:
            fragment: ConfigFragment to move
        """
        file_name = fragment.src.__rname__
        i_dir = f"/usr/local/etc/{constants.INCLUDE_SUBDIR}"
        new_dst = DNSBPath(f"{i_dir}/{file_name}")

        if not self.main:
            return None

        origin = self.fs.read_text(self.main.src)
        pattern = re.compile(r"^\s*include-dir\s*=\s*(.*)", re.IGNORECASE)
        _fnd_icld_dir = []

        for line in origin.splitlines():
            match = pattern.match(line)
            if match:
                _fnd_icld_dir.append(match.group(1))

        write = ""
        if not _fnd_icld_dir:
            write += self._write.format(include_dir=i_dir)
        else:
            if len(_fnd_icld_dir) > 1:
                raise ConfigurationError("Multiple include-dir found")
            if _fnd_icld_dir[0] != i_dir:
                raise ConfigurationError(
                    f"include-dir not match, expected: {i_dir}, actual: {_fnd_icld_dir[0]}"
                )

        _comment = f"{fragment.dst} -> {new_dst}"
        write += self._tmpl.format(config_line=_comment)
        self.fs.append_text(self.main.src, write)

        return ConfigFragment(
            src=fragment.src,
            dst=str(new_dst),
            dcr=fragment.dcr,
            section=fragment.section,
        )

    def _add_include_comment(self, global_config: ConfigFragment, fragment: ConfigFragment):
        """Add include comment to global config."""
        comment = f"\n# include {fragment.dst}"
        self.fs.append_text(global_config.src, comment)


class KnotResolverIncluder(Includer):
    """
    Knot Resolver configuration assembler.

    Knot Resolver uses Lua-based configuration format.
    All includes are done via dofile() function.
    """
    _tmpl = "\n-- Auto Generated by DNS Builder\ndofile('{config_line}')\n"

    def assemble(self) -> None:
        """
        Assemble all pending fragments for Knot Resolver.

        Simply append dofile() directives to the global main config.
        """
        if not self.main:
            logger.warning("No global main config found for Knot Resolver")
            return

        all_fragments = []
        for section in self.get_all_sections():
            all_fragments.extend(self.get_pendings(section))

        for fragment in all_fragments:
            content = self._tmpl.format(config_line=fragment.dst)
            self.fs.append_text(self.main.src, content)

        logger.debug(f"Assembled {len(all_fragments)} fragments for Knot Resolver")


