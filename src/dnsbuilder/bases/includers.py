class UnboundIncluder(Includer):
    """
    Unbound-style configuration assembler.

    For fragments, wrap them with the appropriate section header:
    - global fragments: include directly
    - section fragments: wrap with section template
    """
    _tmpl = '\n# Auto Generated by DNS-Builder\ninclude: "{config_line}"\n'

    def assemble(self) -> None:
        """
        Assemble all pending fragments into the global main config.

        For Unbound, all sections are essentially repeatable (can appear multiple
        times), so we simply append all includes to the global main config.

        Each section fragment gets wrapped with its section header using template.
        """
        if not self.main:
            logger.warning("No global main config found for Unbound")
            return

        for section in self.get_all_sections():
            fragments = self.get_pendings(section)
            if not fragments:
                continue

            for fragment in fragments:
                content = self._format_include_for_section(fragment)
                self.fs.append_text(self.main.src, content)
            logger.debug(f"Appended {len(fragments)} includes for section '{section}'")

    def _format_include_for_section(self, fragment: ConfigFragment) -> str:
        """
        Format an include directive for a fragment using Section template.

        For non-global sections, use the Section's template to wrap the include.
        For global, just output the include directive.

        Args:
            fragment: ConfigFragment to format
        """
        section = fragment.section

        if section == "global":
            return self._tmpl.format(config_line=fragment.dst)
        else:
            # Use Section template to format
            include_line = self._tmpl.format(config_line=fragment.dst)
            section_info = self.get_section_info(section)

            if section_info:
                formatted = section_info.format_content(include_line)
                return f'\n# Auto-Include {section} block by DNS Builder\n{formatted}'
            else:
                # Fallback to simple format
                return (
                    f'\n# Auto-Include {section} block by DNS Builder\n'
                    f'{section}:\n{include_line}'
                )


